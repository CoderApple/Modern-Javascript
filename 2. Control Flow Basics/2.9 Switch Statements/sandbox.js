// switch statement
const grade = 'D';

switch (grade) {
    case 'A':
        console.log("You got an A!");
        break;
    case 'B':
        console.log("You got an B!");
        break;
    case 'C':
        console.log("You got an C!");
        break;
    case 'D':
        console.log("You got an D!");
        break;
    case 'E':
        console.log("You got an E!");
        break;

    default:
        console.log("Invalid grade");
        break;
}

